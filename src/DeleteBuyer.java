    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
         String contactNo1=Contact1.getText();
        int a=JOptionPane.showConfirmDialog(null,"DO you really want to Delete","Select",JOptionPane.YES_NO_OPTION);
        if(a==0)
        {
            try
            {
                Connection con=ConnectionProvider.getCon();
                Statement st=con.createStatement();
                st.executeUpdate("delete from buyer where contactNo='"+contactNo1+"'");
                JOptionPane.showMessageDialog(null,"Successfully Delete Buyer");
                setVisible(false);
                new DeleteBuyer().setVisible(true);
            }
            catch(Exception e)
            {
              JOptionPane.showMessageDialog(null, e);  
            }            
        }
    }//GEN-LAST:event_DeleteActionPerformed

