    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:

        String ProductID =Product.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from product where ProductID='"+ProductID+"'");
            if(rs.next())
            {
                Name.setText(rs.getString(2));
                Rate.setText(rs.getString(3));
                Description.setText(rs.getString(4));
                Activation.setText(rs.getString(5));
                Product.setEditable(false);
            }
            else
            JOptionPane.showMessageDialog(null,"ProductID does not Exit");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SearchActionPerformed

