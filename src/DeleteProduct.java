    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        String ProductID=Product.getText();
       int a=JOptionPane.showConfirmDialog(null,"DO you really want to Delete","Select",JOptionPane.YES_NO_OPTION);
        if(a==0)
        {
            try
            {
                Connection con=ConnectionProvider.getCon();
                Statement st=con.createStatement();
                st.executeUpdate("delete from product where ProductID='"+ProductID+"'");
                JOptionPane.showMessageDialog(null,"Successfully Deleted Product");
                setVisible(false);
                new DeleteProduct().setVisible(true);
            }
            catch(Exception e)
            {
              JOptionPane.showMessageDialog(null, e);  
            }            
        }
    }//GEN-LAST:event_DeleteActionPerformed

